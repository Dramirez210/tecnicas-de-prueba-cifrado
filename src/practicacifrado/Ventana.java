    public static String descifradoCesar(String texto, int desplazamiento) {        
        String salida = "";		
        int aux = mayuscula.length();
        int posicion;
        int nuevaPosicion;

        texto = texto.toUpperCase();

        for (char caracter : texto.toCharArray()) {
            if (caracter == ' ' || caracter == '.' || caracter == ','  || caracter == '\n') {
                salida += caracter;
                continue;
            }
            posicion = caracter - mayuscula.charAt(0);
            nuevaPosicion = Math.floorMod(posicion - desplazamiento, aux);
            salida += mayuscula.charAt(nuevaPosicion);
        }
        return salida;
    }
    
    public static String descifradoVigenere(String texto, String clave) {
        String salida = "";
        int aux = mayuscula.length();
        int abecedario = -1;
        int posicionClave, letraEncriptado, letraClave, letraDesencriptada;
        int resta;  
        
        for (char caracter : texto.toCharArray()) {
            if (caracter == ' ' || caracter == '.' || caracter == ','  || caracter == '\n') {
                salida += caracter;
                continue;
            } 
            abecedario++;
            posicionClave = abecedario % clave.length();
            letraEncriptado = caracter - mayuscula.charAt(0);
            letraClave = clave.charAt(posicionClave) - mayuscula.charAt(0);
            resta = letraEncriptado - letraClave;
            if (resta >= 0) {
                letraDesencriptada = resta % aux;  
            } else {
                letraDesencriptada = (resta + aux) % aux;
            }
            salida += mayuscula.charAt(letraDesencriptada);
        }
        return salida;
    }
    
